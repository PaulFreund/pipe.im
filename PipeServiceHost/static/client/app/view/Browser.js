Ext.define('PipeUI.view.Browser', {
	//------------------------------------------------------------------------------------------------------------------

	extend: 'Ext.tree.Panel',
	xtype: 'pipe-browser',

	//------------------------------------------------------------------------------------------------------------------

	store: 'Services',

	//------------------------------------------------------------------------------------------------------------------

	title: 'Browser',
	rootVisible: false,

	//------------------------------------------------------------------------------------------------------------------

	controller: {
		type: 'baseController',

		onInit: function () {
			this.view.on('select', function (row, record, index, opts) {
				Ext.GlobalEvents.fireEvent('browser_service_selected', record.data.address);
			}, this);
		},

	}

	//------------------------------------------------------------------------------------------------------------------
});

